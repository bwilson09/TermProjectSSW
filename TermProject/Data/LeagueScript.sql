BEGIN TRANSACTION;

DROP TABLE IF EXISTS Team;
DROP TABLE IF EXISTS Player;


CREATE TABLE Team (
	Id INTEGER PRIMARY KEY AUTOINCREMENT,
	Name TEXT NOT NULL,
	Division TEXT,
	RegistrationFee DECIMAL(10,2) DEFAULT 200.0,
	RegistrationPaid BOOLEAN DEFAULT 0,
	PaymentDate DATETIME
);

CREATE TABLE Player (
	Id INTEGER PRIMARY KEY AUTOINCREMENT,
	Name TEXT NOT NULL,
	City TEXT NOT NULL,
	Province TEXT NOT NULL,
	Email TEXT NOT NULL,
	Phone TEXT,
	TeamId INTEGER,
	FOREIGN KEY (TeamId) REFERENCES Team(Id)
);

COMMIT;